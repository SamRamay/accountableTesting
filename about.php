			<p>
        <a href="home.php"><strong>Accountabletesting.com</strong></a> and it’s parent company, <b>Accountable Services, LLC,</b> was formed in 2018 for the <b> purpose of helping people overcome their hang-ups.</b> The founder is a family physician who had his own hang-ups to deal with, and decided it would be great to use his knowledge and experience to organize programs that would help others with theirs. <br>
<br>

In 2007 he was referred to substance abuse treatment for drug and alcohol addiction. After he completed the program, he had to sign a 5 year agreement with the state to be subject to random drug screens among other requirements. He maintained good sobriety during this time, but once he completed his contract, began a slow downhill course back to addiction which culminated in relapse two years after his contract completion. He was eventually referred back to drug/alcohol treatment and completed the program there as well. He was placed on probation afterwards and required to sign another 5 year contract for monitoring. About half-way through this program, he realized that he was receiving advantage from something that the general public was not privy to-his random drug screening. <br>
<br>

Maintaining sobriety is a difficult and complex subject, often with failures along the way. Various forms of treatment include outpatient, intensive outpatient, residential, partial hospitalization, and inpatient hospitalization. Many follow the same twelve step philosophy that programs like Alcoholics Anonymous and Narcotics Anonymous follow. Others follow the Cognitive Behavioral Therapy philosophy, and some are geared towards education, and the list goes on and on. <br>
<br>

Both recovery centers that he went to mixed physicians and nurses with other non-medical patients. What this doctor realized during his second stint in rehab was that the true “recovery” process took place after rehab was completed. This is where, as they say, “the rubber meets the road”, and the principles learned are put into real world practice. Addicts have a large advantage while in treatment, because they are isolated from the drugs/alcohol that was the problem, and they are isolated from the friends and family that may have encouraged it. Once they left, the physicians and nurses were enrolled in monitoring programs, and those that were non-medical personnel were sent out with nothing but the tools they had from rehab. After his own anecdotal evidence, he decided to research what happens to each group. This blurb from an LA Times article sums up most of what he found:
<br><br>
